/**
 * @Author: G_three
 * @Mood: Pumped Up
 * @Date: 2019/7/29 14:08
 **/
@EnableEurekaServer
@SpringBootApplication
public class EurekaApplication {
    public static void main(String[] args) {
        SpringApplication.run(EurekaApplication.class);
    }
}
